/**
 * Traduit un nom de pays anglais en français
 * @param name Nom du pays en anglais
 * @returns Nom traduit en français ou le nom original si non trouvé
 */
export function translateCountryName(name: string): string {
  if (!name) return name
  return COUNTRY_NAMES[name] || name
}

/**
 * Traduit un nom d'équipe (peut être un pays ou un club)
 * Pour les équipes nationales, traduit le nom du pays
 * Pour les clubs, garde le nom original
 * @param teamName Nom de l'équipe
 * @param isNationalTeam Indique si c'est une équipe nationale
 */
export function translateTeamName(teamName: string, isNationalTeam: boolean = false): string {
  if (!teamName) return teamName

  // Si c'est une équipe nationale, tenter de traduire
  if (isNationalTeam) {
    return translateCountryName(teamName)
  }

  // Pour les clubs, vérifier quand même si c'est un nom de pays
  // (peut être utile pour les sélections dans certains contextes)
  const translated = COUNTRY_NAMES[teamName]
  if (translated) {
    return translated
  }

  return teamName
}
